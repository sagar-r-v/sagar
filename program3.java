public class program3
{
    public static void main(String args[])
    {
        int a=5;
        int b=10;
        double c=a+b;
        System.out.println(c);
        System.out.println(a>>b);
        System.out.println(a<<b);
        System.out.println(a++);/*Post-increment*/
        System.out.println(++a);/*Pre-increment*/
        System.out.println(b++);/*Post-increment*/
        System.out.println(++b);/*Pre-increment*/
        System.out.println(a--);/*Post-decrement*/
        System.out.println(--a);/*Pre-decrement*/
        System.out.println(b--);/*Post-deccrement*/
        System.out.println(--b);/*Pre-decrement*/
        System.out.println(a+=10);
    }

}

